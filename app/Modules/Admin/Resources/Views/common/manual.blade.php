    <h2 id="m_nav"><a href="#m_nav"># Навигация</a></h2>
    <p>Навигация по LaraWelt делится на три части: Верхняя панель, боковое меню и область основного контента.</p>
    <p>Верхняя панель имеет несколько вспомогательных ссылок, боковая содержит основные вкладки для перехода по разделам, контентная часть содержит основную область управления модулями.</p>

    <h2 id="m_content"><a href="#m_content"># Контент</a></h2>
    <p>На CMS присутствует возможность управления файлами на сервере. За это отвечают вкладки "Изображения" и "Файлы"</p>
    <p>Советуем Вам разделять файлы по папкам и поддерживать ясную структуру файлов. В другом случае, если складывать все файлы сразу в основную папку, файловый менеджер может потерять быстродействие и скорость загрузки из-за большого количества файлов в одной папке.</p>
