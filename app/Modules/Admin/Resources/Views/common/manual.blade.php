    <h2 id="m_tree"><a href="#m_tree"># Дерево сайта</a></h2>
    <p>Дерево сайта - это отображение всех страниц, присутствующих на сайте. Для страниц предусмотрена возможность редактирования, подключения модулей, распределения страниц по различным меню на сайте.</p>
    <p>Также предусмотрена возможность добавления SEO тегов на страницу для оптимизации сайта для различных поисковых систем.</p>

    <h2 id="m_widgets"><a href="#m_widgets"># Виджеты</a></h2>
    <p>Виджеты - возможность непосредственного управления некоторыми деталями сайта. Они позволяют работать с кодом вёрстки. Для удобной работы с виджетами могут приходиться простейшие знания языка HTML.</p>
    <p>По большей мере виджеты помогают работать с тонкими местами сайта, не требующими отдельных модулей или страниц. К примеру, контактами в футере страницы или группе ссылок в определённом месте на сайте.</p>

    <h2 id="m_modules"><a href="#m_modules"># Модули</a></h2>
    <p>Основная работа пользователя в админ. панели предстоит с модулями сайта. Мы стараемся сделать интерфейс каждого модуля как можно более простым и понятным, в то же время не ограничивая пользователя в свободе настроек как самого модуля, так и отдельных записей.</p>
    <p>Большинство модулей имеет список записей и полную страницу элемента. Из списка можно быстро находить элементы, а так же сортировать, управлять состоянием публикации записи и удалять запись. При добавлении новой записи Вы перейдёте на полную страницу элемента. Там будут находиться поля, некоторые из которых могут быть обязательными для заполнения.</p>
    <p>После заполнения форм не забудьте сохранить запись, а так же пометить галочку "Опубликовать", если запись должна быть опубликована сразу же после сохранения.</p>

